int		ft_is_prime(int nb)
{
	int i;

	i = 2;
	if (nb <= 1)
		return (0);
	while (i <= (nb / i))
	{
		if (nb % i == 0)
		{
			return (0);
		}
		i++;
	}
	return (1);
}

int		ft_find_next_prime(int nb)
{
	int	i;

	i = nb;
	while (i >= nb)
	{
		if (ft_is_prime(i))
			return (i);
		i++;
	}
	return (0);
}
