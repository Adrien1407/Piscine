#ifndef FT_OP_H
# define FT_OP_H

int	ft_1(int a, int b)
{
	return (a + b);
}

int	ft_2(int a, int b)
{
	return (a - b);
}

int	ft_3(int a, int b)
{
	return (a * b);
}

int	ft_4(int a, int b)
{
	return (a % b);
}

int	ft_5(int a, int b)
{
	return (a / b);
}

#endif
